package com;

public class Loop2 {

	public static void main(String[] args) {
	
		
		
		int i=10;
		do//i=1-->2-->3-->4-->5-->6
		{
			System.out.println(i);//10
			i++;//11
		}while(i<=5);
		System.out.println("Value of i after terminating the loop : "+i);
		
/*  1
 *  2 
 *  3
 *  4
 *  5
 * initialization : initial point of loop :starting point
 * condition : terminating point : ending point
 * inc/dec : to increment/decrement : step value
 * 
 * 
 * 
 * 
 * syntax :
 * 
 * initialization
 * while(condition)
 * {
 * 	statement;
 *  inc/dec;
 * }
 * 
 * 		
 */

		
		
	}

}
